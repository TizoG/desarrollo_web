// Operadores aritmeticos

let num1 = 5
let num2 = 10
let suma = num1 + num2
let resta = num1 - num2
let multi = num1 * num2
let divi = num1 / num2
let modulo = num1 % num2
let potencia = num1 ** 2

console.log(suma)
console.log(resta)
console.log(multi)
console.log(divi)
console.log(modulo)
console.log(potencia)

// Operadores de asignación
let a = 5
a = a + 5
a += 5
a -= 5
a *= 5
a /= 5

// Operadores de comparación
let b = 5
let c = 10
console.log(b > c)
console.log(b < c)
console.log(b >= c)
console.log(b <= c)
console.log(b == c)
console.log(b != c)
console.log(b === c)

// Operadores logicos
let d = 5
let e = 10
console.log(d > 5 && e > 5)
console.log(d > 5 || e > 5)
console.log(!(d > 5 && e > 5))
console.log(!(d > 5 || e > 5))

// Operadores de incremento y decremento
let f = 5
console.log(f++)
console.log(f--)
console.log(++f)
console.log(--f)