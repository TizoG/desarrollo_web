// condicional switch

let miDesatuno = 3
switch (miDesatuno) {
    case 1:
        console.log("Desatunado 1")
        break
    case 2:
        console.log("Desatunado 2")
        break
    case 3:
        console.log("Desatunado 3")
        break
    default:
        console.log("Desatunado por defecto")
        break
}